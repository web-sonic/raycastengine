#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*str;

	str = s;
	while (n-- > 0)
		if ((unsigned char)c == *str++)
			return ((void *)(str - 1));
	return (NULL);
}
